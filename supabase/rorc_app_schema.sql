create or replace function public.normalize_membership_account_type(raw_value text)
returns public.membership_account_type
language plpgsql
immutable
as $$
declare
  cleaned text;
begin
  cleaned := lower(regexp_replace(coalesce(raw_value, ''), '\s+', ' ', 'g'));
  cleaned := btrim(cleaned);

  if cleaned = '' then
    return 'Active Membership';
  elsif cleaned in ('active membership', 'active membership ') then
    return 'Active Membership';
  elsif cleaned in ('open gym only', 'open gym sign up', 'open gym signup') then
    return 'Open Gym Only';
  elsif cleaned = 'billed monthly' then
    return 'Billed Monthly';
  elsif cleaned = 'account manager' then
    return 'Account Manager';
  elsif cleaned in (
    'account past due no access allowed',
    'past due balance no access allowed',
    'waiting on contract & payment',
    'waiting on contract and payment',
    'waiting on contract',
    'subscriber account'
  ) or cleaned like '%past due%' or cleaned like '%no access%' then
    return 'Account Past Due NO ACCESS ALLOWED';
  elsif cleaned = 'new member' then
    return 'Active Membership';
  end if;

  return 'Active Membership';
end;
$$;

create or replace function public.normalize_account_billing_status(raw_value text)
returns public.account_billing_status
language plpgsql
immutable
as $$
declare
  cleaned text;
begin
  cleaned := lower(btrim(coalesce(raw_value, '')));

  if cleaned in ('incomplete', 'trialing', 'active', 'past_due', 'canceled', 'unpaid', 'paused') then
    return cleaned::public.account_billing_status;
  elsif cleaned in ('past due', 'past-due') then
    return 'past_due';
  elsif cleaned in ('cancelled', 'cancelled subscription') then
    return 'canceled';
  end if;

  return 'none';
end;
$$;

