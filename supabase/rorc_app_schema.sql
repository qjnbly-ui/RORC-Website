create or replace function public.try_parse_bool(raw_value text)
returns boolean
language plpgsql
immutable
as $$
declare
  cleaned text;
begin
  if raw_value is null or btrim(raw_value) = '' then
    return null;
  end if;

  cleaned := lower(btrim(raw_value));

  if cleaned in ('yes', 'y', 'true', 't', '1', 'on') then
    return true;
  elsif cleaned in ('no', 'n', 'false', 'f', '0', 'off') then
    return false;
  end if;

  return null;
end;
